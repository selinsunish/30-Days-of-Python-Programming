# Python program to find the maximum value in a list

def find_maximum(lst):
    return max(lst)

my_list = [1, 3, 7, 2, 5]
maximum_value = find_maximum(my_list)

print("Maximum value in the list:", maximum_value)

# Python program to find the minimum value in a list

def find_minimum(lst):
    return min(lst)

my_list = [1, 3, 7, 2, 5]
minimum_value = find_minimum(my_list)
print("Minimum value in the list:", minimum_value)

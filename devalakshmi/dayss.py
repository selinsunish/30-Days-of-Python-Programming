day = int(input("Enter a number from 1 to 7: "))

if day == 1:
    print("1 is Sunday")
elif day == 2:
    print("2 is Monday")
elif day == 3:
    print("3 is Tuesday")
elif day == 4:
    print("4 is Wednesday")
elif day == 5:
    print("5 is Thursday")
elif day == 6:
    print("6 is Friday")
elif day == 7:
    print("7 is Saturday")
else:
    print("Invalid input! Please enter a number between 1 and 7.")
